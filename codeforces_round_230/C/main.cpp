namespace solution {
  struct Solution {
    //
    // Implement here
    //
    
    void init() {
    }
    
    void solve() {
      out->result = get_min(in->N);
    }

    Int get_min( Int N ) {
      if ( N == 0 )
        return 1;
      return 4 + 4 * f(N - 1);
    }

    bool check( Double x, Double y ) {
      if ( std::abs( x - y ) < 1e-9 )
        return true;
      return x < y;
    }

    Int f( Double N ) {
      Double lb = 0;
      Double ub = 1000000000LL;
      Double n_sq_2 = N * std::sqrt(2);
      for ( auto i = 0; i < 50; ++ i ) {
        Double mid = ( lb + ub ) / 2.0;
        if ( check(mid, n_sq_2) ) {
          lb = mid;
        } else {
          ub = mid;
        }
      }
      return std::round(lb);
    }
    
    bool input() {
      return std::cin >> in->N;
    }
    
    void output() {
      std::cout << out->result << std::endl;
    }
    
    //
    //
    //
    Solution( Input* in, Output* out ): in(in), out(out) { this->init(); }
    
    int run() {
      for (;;) {
        this->before_action(-1);
        if ( ! this->action() ) { this->after_action(-1); break; }
        this->after_action(-1);
      };
      return 0;
    }
    
    bool action() {
      if ( ! this->input() ) return false;
      this->solve();
      this->output();
      return true;
    }
    
    void before_action( const int& t_id ) {}
    
    void after_action( const int& t_id ) {}
    
    Input* in;
    Output* out;
  };
}

// @snippet<sh19910711/contest-base-simple:main_func.cpp>
int main() {
  std::ios::sync_with_stdio(false);
  std::cin.tie(0);
  using namespace solution;
  static Input in;
  static Output out;
  static Solution sol(&in, &out);
  return sol.run();
}


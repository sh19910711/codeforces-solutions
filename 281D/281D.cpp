namespace solution {
  // constant vars
  const int SIZE = 100000 + 11;
  // storages
  int n;
  int A[SIZE];
  II S[SIZE];

  Set P;
  SetRev P_rev;

  int result;
}

// @snippet<sh19910711/contest:solution/solver-area.cpp>
namespace solution {
  class Solver {
  public:
    void solve() {
      normalize();
      result = get_maximum_xor();
    }

    int get_maximum_xor() {
      int res = 0;

      for ( int i = 0; i < n; ++ i ) {
        int current_value = S[i].first;
        Set::iterator it_after = P.upper_bound(S[i].second);
        Set::iterator it_before = P_rev.upper_bound(S[i].second);

        if ( it_after != P.end() ) {
          int after_value = A[*it_after];
          res = max(res, current_value ^ after_value);
        }

        if ( it_before != P_rev.end() ) {
          int before_value = A[*it_before];
          res = max(res, current_value ^ before_value);
        }

        P.insert(S[i].second);
        P_rev.insert(S[i].second);
      }

      return res;
    }
    
    void normalize() {
      for ( int i = 0; i < n; ++ i ) {
        S[i] = II(A[i], i);
      }
      sort(S, S + n, greater<II>());
    }
    
  private:
    
  };
}

// @snippet<sh19910711/contest:solution/solution.cpp>
namespace solution {
  class Solution: public SolutionBase {
  public:
  protected:
    virtual bool action() {
      init();
      if ( ! input() )
        return false;
      solver.solve();
      output();
      return true;
    }

    void init() {
      result = -1;
      P.clear();
      P_rev.clear();
    }
    
    bool input() {
      if ( ! ( cin >> n ) )
        return false;
      for ( int i = 0; i < n; ++ i )
        cin >> A[i];
      return true;
    }
    
    void output() {
      cout << result << endl;
    }
    
  private:
    Solver solver;
    
  };
}

// @snippet<sh19910711/contest:main.cpp>

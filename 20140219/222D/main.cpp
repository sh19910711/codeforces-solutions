// @snippet<sh19910711/contest-base-simple:solution/solution.cpp>
namespace solution {
  struct Solution {
    //
    // Implement here
    //
 
    void init() {
    }

    Int N;
    Int A[SIZE];
    Int B[SIZE];
    
    void solve() {
      N = in->N;
      std::copy(in->A, in->A + in->N, A);
      std::copy(in->B, in->B + in->N, B);
      std::sort(A, A + N);
      std::sort(B, B + N);
      Int cnt = 0;
      for ( int l = 0, r = N - 1; l < N; ++ l ) {
        if ( A[l] + B[r] >= in->X ) {
          cnt ++;
          r --;
        }
      }
      out->result = cnt;
    }
    
    bool input() {
      if ( ! ( std::cin >> in->N >> in->X ) )
        return false;
      for ( auto i = 0; i < in->N; ++ i )
        if ( ! ( std::cin >> in->A[i] ) )
          return false;
      for ( auto i = 0; i < in->N; ++ i )
        if ( ! ( std::cin >> in->B[i] ) )
          return false;
      return true;
    }
    
    void output() {
      std::cout << 1 << " " << out->result << std::endl;
    }
    
    //
    //
    //
    Solution( Input* in, Output* out ): in(in), out(out) { this->init(); }
    
    int run() {
      for (;;) {
        this->before_action(-1);
        if ( ! this->action() ) { this->after_action(-1); break; }
        this->after_action(-1);
      };
      return 0;
    }
    
    bool action() {
      if ( ! this->input() ) return false;
      this->solve();
      this->output();
      return true;
    }
    
    void before_action( const int& t_id ) {}
    
    void after_action( const int& t_id ) {}
    
    Input* in;
    Output* out;
  };
}

// @snippet<sh19910711/contest-base-simple:main_func.cpp>
int main() {
  std::ios::sync_with_stdio(false);
  std::cin.tie(0);
  using namespace solution;
  static Input in;
  static Output out;
  static Solution sol(&in, &out);
  return sol.run();
}

